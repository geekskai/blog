/**
 * Render modern table using Tailwind CSS
 */
function renderHTMLTableWithTailwind(data: TableData): string {
  const { rows, metadata } = data

  if (!rows || rows.length === 0) {
    return '<div class="text-center text-slate-400 py-8">No data to display</div>'
  }

  // Build table HTML
  const tableClasses = [
    "w-full",
    "border-separate",
    "border-spacing-0",
    "border-2",
    "border-orange-500/40",
    "rounded-2xl",
    "overflow-hidden",
    "shadow-2xl",
    "shadow-black/30",
    "bg-gradient-to-br",
    "from-slate-900/95",
    "to-slate-800/90",
    "backdrop-blur-xl",
  ].join(" ")

  const headerClasses = [
    "bg-gradient-to-r",
    "from-orange-500/30",
    "via-red-500/25",
    "to-pink-500/30",
    "text-white",
    "font-bold",
    "text-sm",
    "uppercase",
    "tracking-wider",
    "px-6",
    "py-4",
    "text-center",
    "border-b-2",
    "border-orange-500/40",
    "border-r",
    "border-orange-500/20",
    "sticky",
    "top-0",
    "z-10",
  ].join(" ")

  const cellClasses = [
    "px-6",
    "py-4",
    "text-white/90",
    "text-sm",
    "border-b",
    "border-orange-500/10",
    "border-r",
    "border-orange-500/8",
    "transition-all",
    "duration-300",
  ].join(" ")

  const rowEvenClasses = [
    "bg-gradient-to-r",
    "from-orange-500/5",
    "via-red-500/3",
    "to-pink-500/5",
  ].join(" ")

  const rowOddClasses = ["bg-gradient-to-r", "from-white/3", "via-orange-500/2", "to-white/3"].join(
    " "
  )

  const rowHoverClasses = [
    "hover:bg-gradient-to-r",
    "hover:from-orange-500/15",
    "hover:via-red-500/10",
    "hover:to-pink-500/12",
    "hover:transform",
    "hover:-translate-y-0.5",
    "hover:shadow-lg",
    "hover:shadow-orange-500/20",
    "cursor-pointer",
  ].join(" ")

  // Generate table headers
  const headers = rows[0]?.map((cell) => cell.value) || []
  const headerRow = headers
    .map(
      (header, index) =>
        `<th class="${headerClasses} ${index === headers.length - 1 ? "border-r-0" : ""}">${header}</th>`
    )
    .join("")

  // Generate data rows
  const dataRows = rows
    .slice(1)
    .map((row, rowIndex) => {
      const isEven = rowIndex % 2 === 0
      const rowBgClass = isEven ? rowEvenClasses : rowOddClasses

      const cells = row
        .map((cell, cellIndex) => {
          const isLastCell = cellIndex === row.length - 1
          const cellClass = `${cellClasses} ${isLastCell ? "border-r-0" : ""}`
          return `<td class="${cellClass}">${cell.value || ""}</td>`
        })
        .join("")

      return `<tr class="${rowBgClass} ${rowHoverClasses} border-b border-orange-500/8">${cells}</tr>`
    })
    .join("")

  const tableHTML = `
    <table class="${tableClasses}">
      <thead>
        <tr>${headerRow}</tr>
      </thead>
      <tbody>
        ${dataRows}
      </tbody>
    </table>
  `

  return createCompleteHTMLDocument(tableHTML)
}

